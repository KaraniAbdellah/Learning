//  Built in Method / Function

public class App {
    public static int sum(int...nums) {
        int s = 0;
        for (int i: nums) {
            s += i;
        }
        return s;
    }
    public static void main(String main[]) {
        System.out.println("The Sum Is " + sum(10, 10, 92, 28));
    }
}


