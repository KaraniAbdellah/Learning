// What is React Query
/*
    React Query is a powerful library developed by 
    TanStack that simplifies data fetching and 
    state management in React applications
*/

// useQuery
/*
    is way to fetch and cache data from server or any async source.
        --> we use useQuery(queryKey, fun)
            [fun]: for fetch data
            [queryKey]: important to query
            [return]: data, error, failureCount, isError, 
                isFetchedAfterMount, isFetching, 
                isIdle, isLoading, isPreviousData, 
                isStale, isSuccess, refetch, remove, status,
*/

